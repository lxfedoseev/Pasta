import Foundation
import UIKit

extension UIColor {
    public class var myRedColor: UIColor
    {
        return UIColor(red: 206/255, green: 43/255, blue: 55/255, alpha: 1.0)
    }
    
    public class var myGrayColor: UIColor
    {
        return UIColor(red: 66/255, green: 66/255, blue: 66/255, alpha: 1.0)
    }
    
    public class var myBlueColor: UIColor
    {
        return UIColor(red: 0/255, green: 97/255, blue: 168/255, alpha: 1.0)
    }
    
    
}
